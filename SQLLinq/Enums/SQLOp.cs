namespace SqlLinqer
{
    /// <summary>
    /// SQL where clause operators
    /// </summary>
    public enum SQLOp
    {
        /// <summary>
        /// Equals
        /// </summary>
        EQ,
        /// <summary>
        /// Greater than
        /// </summary>
        GT,
        /// <summary>
        /// Less than
        /// </summary>
        LT,
        /// <summary>
        /// Greater or equal than
        /// </summary>
        GTE,
        /// <summary>
        /// Less or equal than
        /// </summary>
        LTE,
        /// <summary>
        /// Not
        /// </summary>
        NOT,
        /// <summary>
        /// Like
        /// </summary>
        LIKE,
        /// <summary>
        /// Not Like
        /// </summary>
        NOTLIKE,
        /// <summary>
        /// In
        /// </summary>
        IN,
        /// <summary>
        /// Not In
        /// </summary>
        NOTIN
    }
}
